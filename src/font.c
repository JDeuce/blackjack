static unsigned char g_font_string[] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789-.,:%$";
// % is symbol for 10
void font_get_glyph(unsigned char character, struct GenericGlyph *out) 
{
	unsigned char test_index = 0;
	struct FontGlyph *fg = 0;

	while(g_font_string[test_index] != '\0') {
		if (character == g_font_string[test_index])
			fg = &g_glyphs[test_index];

		test_index++;
	}

	// error condition
	if (fg == 0) {
		out->lines = 0;
		out->width = 0;
		out->height = 0;
	} else {
		out->lines = fg->lines;
		out->width = CHAR_WIDTH;
		out->height = CHAR_HEIGHT;
	}
}



void font_draw_letter_sized(unsigned short x, unsigned short y, 
		      unsigned short forecolor, unsigned short backcolor, 
		      char ch, int size)
{
	struct GenericGlyph glyph;
	font_get_glyph(ch, &glyph);
	glyph_draw(x, y, forecolor, backcolor, size, &glyph);
}

void font_draw_letter(unsigned short x, unsigned short y, 
		      unsigned short forecolor, unsigned short backcolor, 
		      char ch)
{
	font_draw_letter_sized(x, y, forecolor, backcolor, ch, 1);
}

void font_draw_letter_flipped(unsigned short x, unsigned short y, 
		      unsigned short forecolor, unsigned short backcolor, 
		      char ch)
{
	struct GenericGlyph glyph;
	font_get_glyph(ch, &glyph);
	glyph_draw_flipped(x, y, forecolor, backcolor, 1, &glyph);
}

void font_draw_string(unsigned short x, unsigned short y,
		      unsigned short forecolor, unsigned short backcolor,
		      char *string)
{
	unsigned short currentX = x;
	unsigned short currentY = y;

	unsigned short i = 0;
	while (string[i] != '\0')
	{
		if (string[i] != ' ')
			font_draw_letter(currentX, currentY, forecolor, backcolor, string[i]);
		else
			lcd_draw_rect(currentX, currentY, CHAR_HEIGHT, CHAR_WIDTH, backcolor);
		currentY -= CHAR_WIDTH;
		i++;
	}
		
}

// returns width of the string (height is constant)
int font_measure_string(char *string)
{
	int i = 0;
	int width = 0;
	while (string[i] != '\0')
	{
		width += CHAR_WIDTH;
		i++;
	}

	return width;
}

void font_draw_string_flipped(unsigned short x, unsigned short y,
		      unsigned short forecolor, unsigned short backcolor,
		      char *string)
{
	int currentX = x;
	int currentY = y;

	int i = 0;
	while (string[i] != '\0')
	{
		if (string[i] != ' ')
			font_draw_letter_flipped(currentX, currentY, forecolor, backcolor, string[i]);
		currentY -= CHAR_WIDTH;
		i++;
	}
		
}

// convert number to string. assume unsigned 16 bit numbers.
// always pad them to their maximum length (5 characters) with spaces
void itoa(unsigned short number, char result[6])
{
	result[5] = '\0';
	int place;
	// for each digit
	for (place = 4; place >= 0; place--)
	{
		if (number > 0) {
			result[place] = (number % 10) + '0';
			number /= 10;
		} else {
			// no digits left
			// pad with spaces,
			// except for rightmost which should be a 0
			result[place] = (place==4)?'0':' ';
		}
	}
}

void font_draw_number(unsigned short x, unsigned short y,
		      unsigned short forecolor, unsigned short backcolor,
		      unsigned int number)
{
	char result[6];
	itoa(number, result);
	font_draw_string(x, y, forecolor, backcolor, result);
}

void font_draw_number_flipped(unsigned short x, unsigned short y,
		      unsigned short forecolor, unsigned short backcolor,
		      unsigned int number)
{
	char result[6];
	itoa(number, result);
	font_draw_string_flipped(x, y, forecolor, backcolor, result);
}
