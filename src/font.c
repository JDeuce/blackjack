// the lcd likes things defined in quadrant 3 with the top right of
// an object as the reference point.
// glyphs are drawn this way too.
// how should i draw characters? 
// well, char is a glyph so lets orient it the same way, from the top right
// 	- makes some sense
// but what about a string?
//	- using the top right of the string would make some sense, but 
//	requires the size of the string to calculate (pain when manually calculating positions)
//	- using the top left could work, but seems backwards to me working with top right all the
//	time in this code.
// 	- decide to set its reference point to the top right of the first character
//	 for consistency
// 	- don't really like this, better design decision would have been to 
//	put everything oriented from the top left like I'm used to. Oh well.

